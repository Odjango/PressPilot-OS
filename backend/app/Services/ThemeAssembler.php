    private function zipTheme(string $themeDir, string $slug): string
    {
        $zipPath = sys_get_temp_dir()."/pp-themes/{$slug}.zip";
        $zip = new ZipArchive;

        if ($zip->open($zipPath, ZipArchive::CREATE | ZipArchive::OVERWRITE) !== true) {
            throw new RuntimeException('Unable to create zip archive.');
        }

        $files = new \RecursiveIteratorIterator(
            new \RecursiveDirectoryIterator($themeDir, \FilesystemIterator::SKIP_DOTS),
            \RecursiveIteratorIterator::SELF_FIRST
        );

        foreach ($files as $file) {
            $filePath = $file->getRealPath();
            if (! $filePath) {
                continue;
            }

            $relativePath = substr($filePath, strlen($themeDir) + 1);

            if ($file->isDir()) {
                $zip->addEmptyDir($relativePath);
            } else {
                $zip->addFile($filePath, $relativePath);
            }
        }

        $zip->close();

        return $zipPath;
    }

    /**
     * Save logo from data URI or URL to theme assets directory.
     * Sets $project['_logo_saved'] = true if successfully saved.
     */
    private function saveLogoAsset(string $themeDir, array &$project): void
    {
        $logoUrl = $project['logo'] ?? null;
        if (! $logoUrl || ! is_string($logoUrl)) {
            return;
        }

        $assetsDir = $themeDir.'/assets';
        if (! is_dir($assetsDir)) {
            mkdir($assetsDir, 0755, true);
        }

        // Handle data:image URIs (base64 from Studio upload)
        if (str_starts_with($logoUrl, 'data:image/')) {
            // Extract MIME type and extension
            if (preg_match('/^data:image\/(\w+);base64,(.+)$/s', $logoUrl, $matches)) {
                $ext = $matches[1] === 'jpeg' ? 'jpg' : $matches[1]; // normalize jpeg→jpg
                $decoded = base64_decode($matches[2], true);
                if ($decoded !== false) {
                    $filename = "logo.{$ext}";
                    file_put_contents($assetsDir.'/'.$filename, $decoded);
                    $project['_logo_filename'] = $filename;
                    $project['_logo_saved'] = true;
                    Log::info("ThemeAssembler: Logo saved as assets/{$filename}", [
                        'size' => strlen($decoded),
                    ]);

                    return;
                }
            }
            Log::warning('ThemeAssembler: Failed to decode data URI logo');

            return;
        }

        // Handle http(s) URLs — download the file
        if (filter_var($logoUrl, FILTER_VALIDATE_URL)) {
            try {
                $imageData = @file_get_contents($logoUrl, false, stream_context_create([
                    'http' => ['timeout' => 10],
                ]));
                if ($imageData !== false && strlen($imageData) > 0) {
                    // Detect extension from content type or URL
                    $ext = pathinfo(parse_url($logoUrl, PHP_URL_PATH) ?? '', PATHINFO_EXTENSION) ?: 'png';
                    $ext = strtolower($ext);
                    if (! in_array($ext, ['png', 'jpg', 'jpeg', 'gif', 'svg', 'webp'])) {
                        $ext = 'png';
                    }
                    $filename = "logo.{$ext}";
                    file_put_contents($assetsDir.'/'.$filename, $imageData);
                    $project['_logo_filename'] = $filename;
                    $project['_logo_saved'] = true;
                    Log::info("ThemeAssembler: Logo downloaded and saved as assets/{$filename}");

                    return;
                }
            } catch (\Throwable $e) {
                Log::warning('ThemeAssembler: Failed to download logo URL', [
                    'url' => mb_substr($logoUrl, 0, 120),
                    'error' => $e->getMessage(),
                ]);
            }
        }
    }

    /**
     * Get the logo filename from project metadata, with a safe fallback.
     */
    private function getLogoFilename(array $project): string
    {
        return $project['_logo_filename'] ?? 'logo.png';
    }

    /**
     * Check if a logo source is valid for embedding in an <img> src attribute.
     * Accepts both http(s) URLs and data: URIs (base64 logo uploads from Studio).
     */
    private function isValidLogoSource(string $src): bool
    {
        // Accept standard http/https URLs
        if (filter_var($src, FILTER_VALIDATE_URL)) {
            return true;
        }

        // Accept data: URIs (base64-encoded images from Studio upload)
        if (str_starts_with($src, 'data:image/')) {
            return true;
        }

        return false;
    }

    /**
     * Lighten a hex color by blending it with white.
     * Used to generate tertiary color (light tint of primary).
     *
     * @param  string  $hex    Hex color (with or without #)
     * @param  float   $amount Amount to lighten (0.0 = original, 1.0 = white)
     * @return string  Lightened hex color with #
     */
    private function lightenColor(string $hex, float $amount): string
    {
        // Remove # if present
        $hex = ltrim($hex, '#');

        // Convert to RGB
        if (strlen($hex) === 3) {
            $hex = $hex[0].$hex[0].$hex[1].$hex[1].$hex[2].$hex[2];
        }

        $r = hexdec(substr($hex, 0, 2));
        $g = hexdec(substr($hex, 2, 2));
        $b = hexdec(substr($hex, 4, 2));

        // Blend with white (255, 255, 255)
        $r = (int) round($r + ($amount * (255 - $r)));
        $g = (int) round($g + ($amount * (255 - $g)));
        $b = (int) round($b + ($amount * (255 - $b)));

        // Convert back to hex
        return sprintf('#%02x%02x%02x', $r, $g, $b);
    }
