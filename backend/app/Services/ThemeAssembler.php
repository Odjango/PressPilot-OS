<?php

namespace App\Services;

use Illuminate\Support\Facades\Log;
use RuntimeException;
use ZipArchive;

class ThemeAssembler
{
    /**
     * The active proven-core slug. Defaults to 'ollie'.
     * Determines which core's theme.json is loaded and which base files are copied.
     */
    private string $coreSlug = 'ollie';

    /**
     * Set the target proven-core for theme assembly.
     * Must be called before assemble() when using a non-Ollie core.
     */
    public function setCore(string $coreSlug): self
    {
        $this->coreSlug = $coreSlug;
        Log::info('ThemeAssembler: Core set', ['core' => $coreSlug]);

        return $this;
    }

    /**
     * @param  array<string, mixed>  $project
     * @param  array<string, string>  $tokens  All tokens (text + image)
     * @param  array<string, string>  $pageHtml  Injected HTML keyed by page type (home, about, services, contact)
     * @return array{themeDir: string, zipPath: string}
     */
    public function assemble(array $project, array $tokens, array $pageHtml): array
    {
        $slug = $this->resolveSlug($project);
        $themeDir = sys_get_temp_dir()."/pp-themes/{$slug}";

        $this->resetDirectory($themeDir);
        $this->copyBaseFiles($themeDir);
        $this->saveLogoAsset($themeDir, $project);
        $this->writeFunctionsPhp($themeDir, $project);
        $this->writeStyleSheet($themeDir, $project);
        $this->writeThemeJson($themeDir, $project);
        $this->writeTemplates($themeDir, $pageHtml, $project);
        $this->writeParts($themeDir, $project, $tokens);

        $zipPath = $this->zipTheme($themeDir, $slug);

        return [
            'themeDir' => $themeDir,
            'zipPath' => $zipPath,
        ];
    }

    private function resolveSlug(array $project): string
    {
        $slug = $project['slug'] ?? $project['name'] ?? 'presspilot-theme';
        $slug = strtolower(trim((string) $slug));
        $slug = preg_replace('/[^a-z0-9\-]+/', '-', $slug) ?? 'presspilot-theme';

        return trim($slug, '-');
    }

    private function resetDirectory(string $themeDir): void
    {
        if (is_dir($themeDir)) {
            $this->deleteDirectory($themeDir);
        }

        if (! mkdir($themeDir, 0755, true) && ! is_dir($themeDir)) {
            throw new RuntimeException("Unable to create theme directory at {$themeDir}");
        }
    }

    public function deleteDirectory(string $directory): void
    {
        $items = array_diff(scandir($directory) ?: [], ['.', '..']);

        foreach ($items as $item) {
            $path = $directory.DIRECTORY_SEPARATOR.$item;

            if (is_dir($path)) {
                $this->deleteDirectory($path);
            } else {
                unlink($path);
            }
        }

        rmdir($directory);
    }

    private function copyBaseFiles(string $themeDir): void
    {
        $baseDir = base_path('../proven-cores/' . $this->coreSlug);
        $baseFiles = ['index.php'];

        foreach ($baseFiles as $file) {
            $source = $baseDir.DIRECTORY_SEPARATOR.$file;
            $destination = $themeDir.DIRECTORY_SEPARATOR.$file;

            if (! file_exists($source)) {
                throw new RuntimeException("Base file missing: {$source}");
            }

            copy($source, $destination);
        }
    }

    /**
     * Generate functions.php with programmatic content setup.
     *
     * Uses an `init` hook with a one-time execution guard to create pages,
     * set blogname/tagline, create nav menu, and configure the front page.
     * This works in ALL WordPress environments including Playground,
     * unlike starter-content which only works through the Customizer.
     *
     * @param  string  $themeDir
     * @param  array<string, mixed>  $project
     */
    private function writeFunctionsPhp(string $themeDir, array $project): void
    {
        $businessName = addslashes((string) ($project['name'] ?? 'PressPilot'));
        $tagline = addslashes((string) ($project['tagline'] ?? ''));
        $vertical = $project['vertical'] ?? null;

        // Build pages config array
        $pagesConfig = [
            ['title' => 'Home', 'template' => 'front-page'],
            ['title' => 'About', 'template' => 'page-about'],
            ['title' => 'Services', 'template' => 'page-services'],
            ['title' => 'Contact', 'template' => 'page-contact'],
        ];

        if ($vertical === 'restaurant') {
            $pagesConfig[] = ['title' => 'Menu', 'template' => 'page-menu'];
        }

        // Generate PHP array literal for pages
        $pagesPhpArray = "[\n";
        foreach ($pagesConfig as $page) {
            $pagesPhpArray .= "            ['title' => '{$page['title']}', 'template' => '{$page['template']}'],\n";
        }
        $pagesPhpArray .= "        ]";

        $functionsContent = <<<PHP
<?php
/**
 * {$businessName} Theme - Functions
 * Generated by PressPilot
 */

// Programmatic content setup — runs once on theme activation
// Works in all WordPress environments including Playground
add_action('init', function() {
    // One-time guard: only run once per theme activation
    if (get_option('_presspilot_content_loaded')) {
        return;
    }

    // Set site title and tagline
    update_option('blogname', '{$businessName}');
    update_option('blogdescription', '{$tagline}');

    // Remove ALL existing default content (pages + posts)
    // Covers every WordPress environment (standard "Hello world!",
    // Playground "Hello from WordPress Playground!", "Sample Page", etc.)
    \$defaultContent = get_posts([
        'post_type'   => ['post', 'page'],
        'numberposts' => -1,
        'post_status' => 'any',
    ]);
    foreach (\$defaultContent as \$item) {
        wp_delete_post(\$item->ID, true);
    }

    // Define pages to create
    \$pages = {$pagesPhpArray};

    \$createdPages = [];
    \$order = 0;

    foreach (\$pages as \$pageConfig) {
        // Check if page already exists
        \$existing = get_page_by_title(\$pageConfig['title'], OBJECT, 'page');
        if (\$existing) {
            \$createdPages[\$pageConfig['title']] = \$existing->ID;
            // Ensure template and menu order are set
            update_post_meta(\$existing->ID, '_wp_page_template', \$pageConfig['template']);
            wp_update_post(['ID' => \$existing->ID, 'menu_order' => \$order]);
            \$order++;
            continue;
        }

        \$pageId = wp_insert_post([
            'post_title'   => \$pageConfig['title'],
            'post_status'  => 'publish',
            'post_type'    => 'page',
            'page_template' => \$pageConfig['template'],
            'menu_order'   => \$order,
        ]);
        \$order++;

        if (\$pageId && !is_wp_error(\$pageId)) {
            update_post_meta(\$pageId, '_wp_page_template', \$pageConfig['template']);
            \$createdPages[\$pageConfig['title']] = \$pageId;
        }
    }

    // Set Home page as static front page
    if (isset(\$createdPages['Home'])) {
        update_option('show_on_front', 'page');
        update_option('page_on_front', \$createdPages['Home']);
    }

    // Create navigation menu
    \$menuName = 'Primary';
    \$menuExists = wp_get_nav_menu_object(\$menuName);
    if (!\$menuExists) {
        \$menuId = wp_create_nav_menu(\$menuName);
        if (\$menuId && !is_wp_error(\$menuId)) {
            \$order = 0;
            foreach (\$createdPages as \$title => \$pageId) {
                \$order += 10;
                wp_update_nav_menu_item(\$menuId, 0, [
                    'menu-item-title'     => \$title,
                    'menu-item-object'    => 'page',
                    'menu-item-object-id' => \$pageId,
                    'menu-item-type'      => 'post_type',
                    'menu-item-status'    => 'publish',
                    'menu-item-position'  => \$order,
                ]);
            }

            // Assign menu to primary location
            \$locations = get_theme_mod('nav_menu_locations', []);
            \$locations['primary'] = \$menuId;
            set_theme_mod('nav_menu_locations', \$locations);
        }
    }

    // Set up site logo from theme assets
    \$logoFile = get_template_directory() . '/assets/{$this->getLogoFilename($project)}';
    if (file_exists(\$logoFile) && !get_theme_mod('custom_logo')) {
        require_once(ABSPATH . 'wp-admin/includes/image.php');
        require_once(ABSPATH . 'wp-admin/includes/file.php');
        require_once(ABSPATH . 'wp-admin/includes/media.php');

        // Copy to temp location since media_handle_sideload moves the file
        \$tmp = wp_tempnam(\$logoFile);
        copy(\$logoFile, \$tmp);
        \$fileArray = [
            'name'     => basename(\$logoFile),
            'tmp_name' => \$tmp,
        ];
        \$attachmentId = media_handle_sideload(\$fileArray, 0);
        if (!\is_wp_error(\$attachmentId)) {
            set_theme_mod('custom_logo', \$attachmentId);
        }
    }

    // Mark as done so this only runs once
    update_option('_presspilot_content_loaded', true);
}, 1);
PHP;

        file_put_contents($themeDir.'/functions.php', $functionsContent);
    }

