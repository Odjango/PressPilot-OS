    }

    private function writeThemeJson(string $themeDir, array $project): void
    {
        $basePath = base_path('../proven-cores/' . $this->coreSlug . '/theme.json');

        // Fall back to Ollie if the target core has no theme.json (e.g., blockbase)
        if (! file_exists($basePath)) {
            Log::info('ThemeAssembler: No theme.json for core, falling back to Ollie', ['core' => $this->coreSlug]);
            $basePath = base_path('../proven-cores/ollie/theme.json');
        }

        if (! file_exists($basePath)) {
            throw new RuntimeException('Base theme.json not found.');
        }

        $themeJson = json_decode(file_get_contents($basePath), true, 512, JSON_THROW_ON_ERROR);

        // Override colors from project.
        // CRITICAL: Preserve ALL existing palette entries from proven-core theme.json
        // and only override the brand color values. Ollie patterns reference 24+ color slugs.
        $colors = $project['colors'] ?? [];
        $resolver = app(CorePaletteResolver::class);

        // User brand colors (use provided values or defaults)
        $brandPrimary   = $colors['primary'] ?? '#1e3a5f';
        $brandSecondary = $colors['secondary'] ?? '#4a90d9';
        $brandAccent    = $colors['accent'] ?? '#2ecc71';
        $brandBackground = $colors['background'] ?? '#ffffff';
        $brandForeground = $colors['foreground'] ?? '#333333';

        // Generate derived colors
        $tertiary = $this->lightenColor($brandPrimary, 0.85);  // Light tint of primary
        $borderLight = $this->lightenColor($brandPrimary, 0.90); // Very light tint
        $borderDark = $this->lightenColor($brandPrimary, 0.70);  // Medium tint
        $primaryAccent = $this->lightenColor($brandPrimary, 0.80); // Light accent
        $mainAccent = $this->lightenColor($brandForeground, 0.40); // Lighter text

        // Build COMPLETE Ollie-compatible palette with all slugs that patterns reference
        // This ensures no "Attempt Recovery" errors from missing color variables
        $fullPalette = [
            // Core brand colors (user-provided)
            'primary'               => $brandPrimary,
            'secondary'             => $brandSecondary,
            'primary-alt'           => $brandAccent,
            'accent'                => $brandAccent,
            'base'                  => $brandBackground,
            'main'                  => $brandForeground,

            // Additional brand variants (auto-generated)
            'tertiary'              => $tertiary,
            'foreground'            => $brandForeground,
            'background'            => $brandBackground,
            'contrast'              => '#1a1a1a',

            // Border colors (derived from primary)
            'border'                => $borderLight,
            'border-light'          => $borderLight,
            'border-dark'           => $borderDark,

            // Accent variants (derived)
            'primary-accent'        => $primaryAccent,
            'main-accent'           => $mainAccent,

            // Background variants (for has-*-background-color classes)
            'primary-background'    => $this->lightenColor($brandPrimary, 0.95),
            'secondary-background'  => $this->lightenColor($brandSecondary, 0.95),
            'tertiary-background'   => $this->lightenColor($tertiary, 0.50),
            'base-background'       => $brandBackground,
            'main-background'       => $this->lightenColor($brandForeground, 0.95),
            'border-light-background' => $this->lightenColor($borderLight, 0.50),
            'border-dark-background'  => $this->lightenColor($borderDark, 0.50),

            // Border variants for has-*-border-color classes
            'tertiary-border'       => $tertiary,
            'border-light-border'   => $borderLight,
            'primary-border'        => $brandPrimary,
            'base-border'           => $this->lightenColor($brandBackground, 0.90),

            // Icon and utility colors
            'icon'                  => $brandPrimary,
            'icon-background'       => $this->lightenColor($brandPrimary, 0.95),
            'link'                  => $brandPrimary,
            'text'                  => $brandForeground,
            'inline'                => $brandForeground,
        ];

        // Resolve canonical slugs to target core slugs
        $paletteOverrides = [];
        foreach ($fullPalette as $canonicalSlug => $colorValue) {
            $targetSlug = $resolver->resolve($canonicalSlug, $this->coreSlug);
            $paletteOverrides[$targetSlug] = $colorValue;
        }

        // Step 1: Update existing palette entries from base theme.json with brand colors
        if (isset($themeJson['settings']['color']['palette'])) {
            foreach ($themeJson['settings']['color']['palette'] as $index => $entry) {
                $slug = $entry['slug'] ?? null;
                if ($slug && array_key_exists($slug, $paletteOverrides)) {
                    // Override this color with user brand value
                    $themeJson['settings']['color']['palette'][$index]['color'] = $paletteOverrides[$slug];
                    unset($paletteOverrides[$slug]); // Mark as processed
                }
            }
        } else {
            $themeJson['settings']['color']['palette'] = [];
        }

        // Step 2: Add any missing slugs that patterns reference but aren't in base theme.json
        foreach ($paletteOverrides as $slug => $color) {
            $themeJson['settings']['color']['palette'][] = [
                'slug'  => $slug,
                'color' => $color,
                'name'  => ucfirst(str_replace('-', ' ', $slug)),
            ];
            Log::info("ThemeAssembler: Added missing palette slug '{$slug}' = {$color}");
        }

        // Override font families (heading + body pairing if available, else single font)
        $fontPairing = $project['fontPairing'] ?? null;
        $fontFamily = $project['fontFamily'] ?? ($project['fonts']['primary'] ?? null);

        if ($fontPairing && isset($themeJson['settings']['typography']['fontFamilies'])) {
            // Replace first font family with heading font
            $themeJson['settings']['typography']['fontFamilies'][0] = [
                'fontFamily' => $fontPairing['heading'] . ', system-ui, sans-serif',
                'name' => $fontPairing['heading'],
                'slug' => 'heading',
            ];

            // Add body font as second family (or update existing)
            $bodyEntry = [
                'fontFamily' => $fontPairing['body'] . ', system-ui, sans-serif',
                'name' => $fontPairing['body'],
                'slug' => 'body',
            ];

            // If heading and body are same font, just use one entry with slug "primary"
            if ($fontPairing['heading'] === $fontPairing['body']) {
                $themeJson['settings']['typography']['fontFamilies'][0]['slug'] = 'primary';
            } else {
                // Ensure body font is second entry, replacing or adding
                if (isset($themeJson['settings']['typography']['fontFamilies'][1])) {
                    $themeJson['settings']['typography']['fontFamilies'][1] = $bodyEntry;
                } else {
                    $themeJson['settings']['typography']['fontFamilies'][] = $bodyEntry;
                }
            }

            Log::info('ThemeAssembler: Applied font pairing', [
                'heading' => $fontPairing['heading'],
                'body' => $fontPairing['body'],
            ]);
        } elseif ($fontFamily && isset($themeJson['settings']['typography']['fontFamilies'][0])) {
            // Legacy: single font override (backward compatible)
            $themeJson['settings']['typography']['fontFamilies'][0]['fontFamily'] = $fontFamily . ', system-ui, sans-serif';
            $themeJson['settings']['typography']['fontFamilies'][0]['name'] = $fontFamily;
        }

        // Add custom templates registration
        $customTemplates = [
            ['name' => 'page-about', 'title' => 'About', 'postTypes' => ['page']],
            ['name' => 'page-services', 'title' => 'Services', 'postTypes' => ['page']],
            ['name' => 'page-contact', 'title' => 'Contact', 'postTypes' => ['page']],
        ];

        // Add menu template if this is a restaurant vertical
        $vertical = $project['vertical'] ?? null;
        if ($vertical === 'restaurant') {
            $customTemplates[] = ['name' => 'page-menu', 'title' => 'Menu', 'postTypes' => ['page']];
        }

        $themeJson['customTemplates'] = $customTemplates;

        // Ensure templateParts are registered
        $themeJson['templateParts'] = [
            ['name' => 'header', 'area' => 'header', 'title' => 'Header'],
            ['name' => 'header-transparent', 'area' => 'header', 'title' => 'Header Transparent'],
            ['name' => 'footer', 'area' => 'footer', 'title' => 'Footer'],
        ];

        // Add layout settings (contentSize and wideSize)
        if (!isset($themeJson['settings']['layout'])) {
            $themeJson['settings']['layout'] = [];
        }
        $themeJson['settings']['layout']['contentSize'] = '1200px';
        $themeJson['settings']['layout']['wideSize'] = '1400px';

        file_put_contents($themeDir.'/theme.json', json_encode($themeJson, JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES));
    }

    /**
     * Write all template files from injected page HTML.
     *
     * @param  array<string, string>  $pageHtml  Keyed by page type
     * @param  array<string, mixed>  $project  Project data including heroLayout
     */
    private function writeTemplates(string $themeDir, array $pageHtml, ?array $project = null): void
    {
        $templatesDir = $themeDir.'/templates';
        if (! is_dir($templatesDir)) {
            mkdir($templatesDir, 0755, true);
        }

        // front-page.html — uses home page sections
        // For fullBleed hero layout, the hero skeleton already contains its own
        // header (site-title + navigation) inside the wp:cover block, so we must
        // skip the external header-transparent template part to avoid TWO headers.
        $homeContent = $pageHtml['home'] ?? '';
        $heroLayout  = $project['heroLayout'] ?? $project['hero_layout'] ?? null;
        $skipHeader  = ($heroLayout === 'fullBleed');

        Log::info('ThemeAssembler::writeTemplates heroLayout decision', [
            'heroLayout' => $heroLayout,
            'skipHeader' => $skipHeader,
        ]);

        file_put_contents($templatesDir.'/front-page.html', $this->wrapTemplate($homeContent, $skipHeader));

        // Inner pages use the standard (opaque) header since page-banner provides
        // the visual impact. This gives a clean white header → colored banner flow.
        $aboutContent = $pageHtml['about'] ?? '';
        file_put_contents($templatesDir.'/page-about.html', $this->wrapTemplate($aboutContent, false, 'header'));

        if (isset($pageHtml['menu'])) {
            $menuContent = $pageHtml['menu'];
            file_put_contents($templatesDir.'/page-menu.html', $this->wrapTemplate($menuContent, false, 'header'));
        }

        $servicesContent = $pageHtml['services'] ?? '';
        file_put_contents($templatesDir.'/page-services.html', $this->wrapTemplate($servicesContent, false, 'header'));

        $contactContent = $pageHtml['contact'] ?? '';
        file_put_contents($templatesDir.'/page-contact.html', $this->wrapTemplate($contactContent, false, 'header'));

        // Generic fallback templates
        file_put_contents($templatesDir.'/page.html', $this->defaultPageTemplate());
        file_put_contents($templatesDir.'/single.html', $this->defaultSingleTemplate());
        file_put_contents($templatesDir.'/404.html', $this->default404Template());
        file_put_contents($templatesDir.'/index.html', $this->defaultIndexTemplate());
    }

    /**
     * Write header and footer parts.
     */
    private function writeParts(string $themeDir, array $project, array $tokens): void
    {
        $partsDir = $themeDir.'/parts';
        if (! is_dir($partsDir)) {
            mkdir($partsDir, 0755, true);
        }

        file_put_contents($partsDir.'/header.html', $this->buildHeader($project));
        file_put_contents($partsDir.'/header-transparent.html', $this->buildHeaderTransparent($project));
        file_put_contents($partsDir.'/footer.html', $this->buildPressPilotFooter($project, $tokens));
    }

