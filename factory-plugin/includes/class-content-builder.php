<?php
/**
 * Content Builder - Creates pages with wp_insert_post using patterns
 *
 * @package PressPilot_Factory
 */

if ( ! defined( 'ABSPATH' ) ) {
    exit;
}

class PressPilot_Factory_Content_Builder {

    private $pattern_loader;

    public function __construct( PressPilot_Factory_Pattern_Loader $pattern_loader ) {
        $this->pattern_loader = $pattern_loader;
    }

    /**
     * Create a page with patterns
     */
    public function create_page( $title, $slug, $patterns, $params ) {
        // Build page content from patterns
        $content = $this->build_content( $patterns, $params );

        // Prepare post data
        $post_data = [
            'post_title'   => $title,
            'post_name'    => $slug,
            'post_content' => $content,
            'post_status'  => 'publish',
            'post_type'    => 'page',
            'post_author'  => get_current_user_id() ?: 1,
        ];

        // Check if page exists
        $existing = get_page_by_path( $slug );
        if ( $existing ) {
            $post_data['ID'] = $existing->ID;
            $page_id = wp_update_post( $post_data );
        } else {
            $page_id = wp_insert_post( $post_data );
        }

        if ( is_wp_error( $page_id ) ) {
            return false;
        }

        // Mark as generated by PressPilot
        update_post_meta( $page_id, '_presspilot_generated', true );
        update_post_meta( $page_id, '_presspilot_generation_time', current_time( 'mysql' ) );
        update_post_meta( $page_id, '_presspilot_patterns', $patterns );

        return $page_id;
    }

    /**
     * Build content from patterns
     */
    private function build_content( $patterns, $params ) {
        $content_parts = [];

        foreach ( $patterns as $pattern_name ) {
            $pattern_content = $this->pattern_loader->load_and_render( $pattern_name, $params );
            if ( $pattern_content ) {
                $content_parts[] = $pattern_content;
            }
        }

        return implode( "\n\n", $content_parts );
    }

    /**
     * Create WooCommerce pages if needed
     */
    public function create_woocommerce_pages( $params ) {
        if ( ! class_exists( 'WooCommerce' ) ) {
            return [];
        }

        $pages = [];

        // Shop page (uses WooCommerce shortcode)
        $shop_content = $this->get_shop_content( $params );
        $shop_id = $this->create_page( 'Shop', 'shop', [], $params );
        if ( $shop_id ) {
            wp_update_post([
                'ID'           => $shop_id,
                'post_content' => $shop_content,
            ]);
            update_option( 'woocommerce_shop_page_id', $shop_id );
            $pages['shop'] = $shop_id;
        }

        // Cart page
        $cart_id = $this->create_page( 'Cart', 'cart', [], $params );
        if ( $cart_id ) {
            wp_update_post([
                'ID'           => $cart_id,
                'post_content' => '<!-- wp:woocommerce/cart /-->',
            ]);
            update_option( 'woocommerce_cart_page_id', $cart_id );
            $pages['cart'] = $cart_id;
        }

        // Checkout page
        $checkout_id = $this->create_page( 'Checkout', 'checkout', [], $params );
        if ( $checkout_id ) {
            wp_update_post([
                'ID'           => $checkout_id,
                'post_content' => '<!-- wp:woocommerce/checkout /-->',
            ]);
            update_option( 'woocommerce_checkout_page_id', $checkout_id );
            $pages['checkout'] = $checkout_id;
        }

        // My Account page
        $account_id = $this->create_page( 'My Account', 'my-account', [], $params );
        if ( $account_id ) {
            wp_update_post([
                'ID'           => $account_id,
                'post_content' => '<!-- wp:woocommerce/my-account /-->',
            ]);
            update_option( 'woocommerce_myaccount_page_id', $account_id );
            $pages['my-account'] = $account_id;
        }

        return $pages;
    }

