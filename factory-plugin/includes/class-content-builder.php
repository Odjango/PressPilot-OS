    }

    /**
     * Create restaurant menu page
     */
    public function create_menu_page( $params ) {
        $menu_items = $params['page_content']['items'] ?? [
            [ 'name' => 'Signature Dish', 'description' => 'Our chef\'s special creation', 'price' => '$24.00' ],
            [ 'name' => 'Classic Favorite', 'description' => 'A timeless customer favorite', 'price' => '$18.00' ],
            [ 'name' => 'Seasonal Special', 'description' => 'Fresh seasonal ingredients', 'price' => '$22.00' ],
        ];

        $content = $this->build_menu_content( $params, $menu_items );

        return $this->create_page( 'Menu', 'menu', [], array_merge( $params, [ 'raw_content' => $content ] ) );
    }

    /**
     * Build restaurant menu content
     */
    private function build_menu_content( $params, $items ) {
        $headline = $params['page_content']['headline'] ?? 'Our Menu';

        $content = '<!-- wp:group {"align":"full","style":{"spacing":{"padding":{"top":"var:preset|spacing|60","bottom":"var:preset|spacing|60"}}},"layout":{"type":"constrained"}} -->
<div class="wp-block-group alignfull" style="padding-top:var(--wp--preset--spacing--60);padding-bottom:var(--wp--preset--spacing--60)">
<!-- wp:heading {"textAlign":"center","level":1} -->
<h1 class="wp-block-heading has-text-align-center">' . esc_html( $headline ) . '</h1>
<!-- /wp:heading -->

<!-- wp:spacer {"height":"40px"} -->
<div style="height:40px" aria-hidden="true" class="wp-block-spacer"></div>
<!-- /wp:spacer -->';

        foreach ( $items as $item ) {
            $content .= '
<!-- wp:group {"style":{"spacing":{"padding":{"top":"var:preset|spacing|30","bottom":"var:preset|spacing|30"}},"border":{"bottom":{"color":"var:preset|color|secondary","width":"1px"}}},"layout":{"type":"flex","flexWrap":"nowrap","justifyContent":"space-between"}} -->
