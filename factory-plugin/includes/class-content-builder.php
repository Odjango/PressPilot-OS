<!-- /wp:group -->';
        }

        $content .= '
</div>
<!-- /wp:group -->';

        return $content;
    }

    /**
     * Delete all generated pages
     */
    public function delete_generated_pages() {
        $args = [
            'post_type'      => 'page',
            'posts_per_page' => -1,
            'meta_key'       => '_presspilot_generated',
            'meta_value'     => true,
            'fields'         => 'ids',
        ];

        $pages = get_posts( $args );

        foreach ( $pages as $page_id ) {
            wp_delete_post( $page_id, true );
        }

        return count( $pages );
    }
}
